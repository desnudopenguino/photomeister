<h1>FAQ</h1>
<p>Here are some common questions and answers to them.</p>

<h2>What is PhotoMeister?</h2>
<p>PhotoMeister is an ever evolving photo portfolio site where you can share your photographs
	with everyone on the internet.  The philosophy of PhotoMeister is simplicity, and ease of
	use.
</p>

<h2>What makes PhotoMeister different than other photo sites?</h2>
<p>PhotoMeister is a site build to be easily changed and "evolve" with our members' needs.  This
	enables us to be able to change settings easily and create new functionality easily that our
	users request.
</p>
<h2>How do I use PhotoMeister?</h2>
<p>If you want to use PhotoMeister to share your photos, you have to register first.   Then you
	can create different portfolios.  These portfolios are used to help organize photo albums.
	Photo albums can be public or private!  So you can let the albums be viewable by the world, or
	only people that you give the password to!  You can upload as many images as you want to an
	album, and you can organize the order of your images by dragging them and dropping them into 
	the order you want.
</p>
